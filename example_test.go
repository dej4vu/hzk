package hzk

import (
	"fmt"
	"log"
)

func ExampleMatrix() {
	var str = "我"
	matrix, err := Matrix([]byte(str))
	if err != nil {
		log.Fatal(err)
	}
	var key = []byte{0x80, 0x40, 0x20, 0x10, 0x08, 0x04, 0x02, 0x01}
	for k := 0; k < 16; k++ {
		for j := 0; j < 2; j++ {
			for i := 0; i < 8; i++ {
				if flag := (matrix[2*k+j] & key[i]) == 0; flag {
					fmt.Print("●")
				} else {
					fmt.Print("○")
				}
			}
		}
		fmt.Println()
	}
	// Output:
	// ●●●●●○●●○●●●●●●●
	// ●●●●○○○●○●○●●●●●
	// ●○○○○●●●○●●○●●●●
	// ●●●●○●●●○●●○●●●●
	// ●●●●○●●●○●●●●○●●
	// ○○○○○○○○○○○○○○○●
	// ●●●●○●●●○●●●●●●●
	// ●●●●○●●●○●●○●●●●
	// ●●●●○●○●○●●○●●●●
	// ●●●●○○●●●○○●●●●●
	// ●●●○○●●●●○●●●●●●
	// ●○○●○●●●○●○●●●●●
	// ●●●●○●●○●●○●●●●●
	// ●●●●○●○●●●●○●○●●
	// ●●○●○●●●●●●○●○●●
	// ●●●○●●●●●●●●○○●●
}
